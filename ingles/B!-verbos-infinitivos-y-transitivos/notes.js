/*10 Until and till

Let's sort this out once and for all:
    is there a difference between until and till ?

Untill :
    es una preposición que expresa que algo sucederá hasta un momento dado. Es decir, alguna acción ha comenzado y terminará en el momento mencionado.

    For example, we may say:

    1. Can you wait until I finish eating my lunch ?
    ¿Puedes esperar hasta que termine de comer mi almuerzo?

    2. The show will be live until midnight.
    El programa estará en vivo hasta la medianoche.

Till :
    se puede usar de la misma manera que Untill, pero se percibe como menos formal. Esta formalidad es realmente la única diferencia entre las dos palabras.

    Some sentences in which we might use till are:

    1. I'm going out till the sun comes out.
    Voy a salir hasta que salga el sol.

    2. I'll be out of town till tomorrow.
    Estaré fuera de la ciudad hasta mañana.
*/
/*12 Prohibition -- muar and  mustn't

Must / musn’t sirve para hablar acerca de una regla u orden, algo que es obligatorio de realizar. Puede usarse particularmente para prohibir algo en un lugar.

Ejemplos usando must/musn’t
What mustn’t we do in the restaurant?

We mustn’t leave without paying.
We must be respectful to the server.
We mustn’t throw the food in the air.
What must we do at school or university?

We must respect the teacher.
We must do our homework.
We must be on time.
What must we do at the airport?

We must bring our passport or identity card?
We must be on time.
We must be patient, because sometimes there are a lot of delays.
What must we do at the park?

We mustn’t throw garbage on the ground.
We must have fun.
We mustn’t damage the plants.

*/
/*13 Speculations --May, might y could

May, might y could son verbos modales que sirven para especular sobre acciones del futuro; y representan una probabilidad alta, media y baja, respectivamente. Tener en cuenta que no se refieren a un evento que seguramente pasará.

It may rain tomorrow (seguro que lloverá).
It might rain tomorrow (tal vez llueva).
It could rain tomorrow (podría llover).

Ejemplos usando may, might y could

I may go to the gym tomorrow morning.
Puedo ir al gimnasio mañana por la mañana.

She might call him back.
Ella podría devolverle la llamada.

They could leave one hour earlier to avoid the traffic jams.
Podrían salir una hora antes para evitar los atascos.

*/
